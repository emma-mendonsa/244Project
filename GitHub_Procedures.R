#Opening GitHub file on Rstudio
# In GitHub;
# 1) Get to repository/main project folder
# 2) Click on green "Clone or Download" button
# 3) Copy HTTPS

# In RStudio:
# 1) Click File
# 2) Add New Project
# 3) Select "Version Control" type
# 4) Paste HTTPS into Pop-Up
# 5) Choose where you want the project to save via "Browse"
# 6) Click "Create Project"
# 7) RStudio will download the GitHub info

#"Push"/Send Updates to GIT Procedure: 
# 1) Add code
# 2) Save code
# 3) Press commit
# 4) Add comment in pop-up
# 5) Click "Stage All" button
# 6) Press commit button
# 7) Press green arrow "Push"
# 8) See pop-up confirming code sent to Git
# 9) Go online and see the updated code

#"Pulled"/Downloaded code from GIT Procedure:
# In RStudio,
# 1) Under Git tab, press blue down arrow
# 2) Pop up will show file downloading
# 3) File on computer will update automatically
# 4) Begin adding to the code :)